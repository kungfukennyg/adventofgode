func Test_boxCoordinates(t *testing.T) {
	type args struct {
		input string
	}
	tests := []struct {
		args args
		want int
	}{
		{
			args: args{`########
#..O.O.#
##@.O..#
#...O..#
#.#.O..#
#...O..#
#......#
########

<^^>>>vv<v>>v<<`},
			want: 2028,
		},
		{
			args: args{testInput},
			want: 10092,
		},
		{
			args: args{aoc.Input()},
			want: 1442192,
		},
	}
	for i, tt := range tests {
		t.Run(strconv.Itoa(i), func(t *testing.T) {
			if got := boxCoordinates(tt.args.input); got != tt.want {
				t.Errorf("boxCoordinates() = %v, want %v", got, tt.want)
			}
		})
	}
}

